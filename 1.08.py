num = float(input("Enter a number: "))
    
if 2 <= num <= 8:
    print("The number is between 2 and 8.")
elif num > 11:
    print("The number is bigger than 11.")
elif num < -2:
    print("The number is smaller than -2.")
else:
    print("The number does not meet any of the specified conditions.")

    
